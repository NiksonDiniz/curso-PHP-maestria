<div class="titulo">mysqli x PDO</div>

<ul>
  <li>O mysqli é uma extensão do próprio PHP para conectar ao banco MySQL, e tem uma proximidade do código nativo, sendo mais rápida do que a PDO;</li>
  <li>PDO é uma API para conexão de bancos de dados, não limitada ao MySQL, que abstrair alguns conceitos com código PHP tornando esta abordagem mais lenta;</li>
  <li>Ambas as formas podem ser utilizadas da forma orientada a objetos;</li>
</ul>

