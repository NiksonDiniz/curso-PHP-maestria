<div class="titulo">Interface do DAO</div>

<ul>
  <li>Normalmente também é criada uma interface para o DAO;</li>
  <li>Esta interface molda a classe DAO, definindo seus métodos;</li>
  <li>Desta maneira temos um esqueleto para seguir e implementar na classe que vai manipular o banco de dados;</li>
  <li>Os métodos principais da interface são pelo menos os que constituem o CRUD;</li>
  <li>A interface pode ser re-implementada em diversos bancos, por exemplo;</li>
</ul>