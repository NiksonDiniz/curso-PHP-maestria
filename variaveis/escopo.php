<div class="titulo">Escopo</div>

<ul>
  <li>Como em outras linguagens em PHP também temos escopo de variáveis;</li>
  <li>Local: variável declarada em uma função;</li>
  <li>Global: variáveis declaradas fora de funções;</li>
  <li>Static: variável declarada dentro da função, porém o seu valor permanece salvo entre chamadas da função;</li>
  <li>Parâmetros de função: variáveis passadas para uma função, podendo ser utilizadas ao longo da mesma;</li>
</ul>

