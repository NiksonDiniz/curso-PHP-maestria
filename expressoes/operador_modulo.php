<div class="titulo">Operador de módulo (%)</div>

<ul>
  <li>O operador de módulo é inserido no código pelo símbolo de %</li>
  <li>Sua função é realizar uma divisão;</li>
  <li>Mas como resultado ele apresenta apenas o resto da mesma;</li>
</ul>

<?php
  echo 12 % 2 . " módulo <br>";
  echo 12 / 2 . " divisão <br>";
  echo 13 % 2 . " módulo <br>";
  echo 13 % 5 . " módulo <br><br>";
?>
