<div class="titulo">Operadores lógicos</div>

<ul>
  <li>Com os operadores lógicos podemos encadear várias comparações;</li>
  <li>Operador AND: &&</li>
  <li>Operador OR: ||</li>
  <li>Operador NOT: !</li>
</ul>

<div class="titulo">Operador lógico AND</div>

<ul>
  <li>Os operadores lógicos em conjunto dos de comparação também retornam uma booleano (true ou false);</li>
  <li>No caso de AND temos true apenas quando as duas comparações são verdadeiras;</li>
  <li>Símbolo: &&</li>
  <li>Ex: 5 > 2 && 10 < 100 # true</li>
</ul>

<div class="titulo">Operador lógico OR</div>

<ul>
  <li>O operador lógico OR resulta em verdadeiro caso qualquer um dos lados da operação seja verdadeiro;</li>
  <li>E só resulta em falso caso os dois lados sejam falsos;</li>
  <li>Símbolo: ||</li>
  <li>Exemplo: 5 > 15 || “teste” == “teste” # true</li>
</ul>

<div class="titulo">Operador lógico NOT</div>

<ul>
  <li>O operador lógico NOT apenas inverte o resultado booleano de uma operação, se é true vira false e se é false vira true;</li>
  <li>Símbolo: !</li>
  <li>Exemplo: !true # false</li>
  <li>Exemplo: !(5 > 2) # false</li>
</ul>