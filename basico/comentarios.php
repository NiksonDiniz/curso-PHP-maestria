<div class="titulo">Comentários PHP</div>

<ul>
  <li>Os comentários servem para dar informações e direções importantes de como o código funciona;</li>
  <li>Iniciamos um comentário com //</li>
  <li>Todo conteúdo que está em um comentário é ignorado na execução;</li>
  <li>Não insira informações sensíveis nos comentários;</li>
  <li>Outra forma de inserir comentários é com #</li>
  <li>Comentários multi linhas são feitos com: /* comentário */</li>
</ul>

<?php
  // comentário de uma linha
  # comentário de uma linha
  /* 
    comentário multi linhas
  */
