<div class="titulo">Processamento de formulários teoria</div>

<ul>
  <li>Vamos realizar processamentos de formulário de duas maneiras: via GET e via POST;</li>
  <li>Com o GET vamos processar os parâmetros que vem na query string, ou seja, na URL;</li>
  <li>Com o POST vamos processar as informações que vem na requisição, estas não aparecem na URL;</li>
  <li>Exemplos de uso: Buscas => GET, Registro de usuário => POST</li>
</ul>


