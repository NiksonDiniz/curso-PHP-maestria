<div class="titulo">Variáveis globais do PHP</div>

<ul>
  <li>Para lidar com estas requisições o PHP nos dá algumas variáveis globais:</li>
  <li>$_ENV => variáveis de ambiente;</li>
  <li>$_GET = Parâmetros que foram enviados por request GET;</li>
  <li>$_POST => Parâmetros que foram enviados por POST;</li>
  <li>$_COOKIE => Valores de cookies;</li>
  <li>$_SERVER => Informações sobre o servidor;</li>
  <li>$_FILES => Informações sobre os arquivos que vieram por upload;</li>
</ul>