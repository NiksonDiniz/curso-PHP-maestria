<div class="titulo">Instanciando objetos</div>

<ul>
  <li>Para instanciar um objeto vamos utilizar a palavra new em conjunto do nome da classe;</li>
  <li>A partir daí uma entidade com as características da classe será criada;</li>
  <li>Normalmente encapsulamos este valor em uma variável;</li>
  <li>Exemplo: <br>$matheus = new User;</li>
</ul>

<?php

  class User {};

  $goku = new User;
  $gohan = new User;
  $gotem = new User;
