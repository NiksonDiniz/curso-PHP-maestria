<div class="titulo">O que são objetos?</div>

<ul>
  <li>Objetos são entidades que possuem comportamentos e características;</li>
  <li>As características são conhecidas como propriedades (variáveis);</li>
  <li>Os comportamentos como métodos (funções);</li>
  <li>Os objetos interagem entre si e sistemas são escritos orientados a objetos (paradigma de Orientação a Objetos);</li>
  <li>No PHP podemos desenvolver neste paradigma;</li>
</ul>