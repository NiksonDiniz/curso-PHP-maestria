<div class="titulo">PHP e Banco de Dados</div>

<ul>
  <li>O PHP tem suporte para mais de 20 bancos de dados;</li>
  <li>O mais comum a ser utilizado é o MySQL;</li>
  <li>Há algumas formas de conexão a bancos disponíveis no PHP, a mais famosa é o PDO (PHP Data Objects), porém também temos a mysqli;</li>
  <li>PDO costuma ser implementado por causa da abordagem orientada a objetos e outras vantagens sobre a mysqli;</li>
</ul>

<div class="titulo">Banco de dados Relacionais</div>

<ul>
  <li>O banco de dado relacional tem sua principal característica trabalhar com tabelas;</li>
  <li>Onde ela possui colunas que categorizam os dados, que são inseridos nas tabelas;</li>
  <li>O PHP é muito utilizado com DBs relacionais, como o MySQL;</li>
  <li>A linguagem para operações com estes bancos é a SQL;</li>
  <li>As instruções em SQL costumam ser escritas em letras maiúsculas;</li>
</ul>