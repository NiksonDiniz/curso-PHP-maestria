<div class="titulo">Deletando dados</div>

<ul>
  <li>Para deletar dados de uma tabela vamos utilizar o DELETE FROM;</li>
  <li>Obs: DELETE sem WHERE vai deletar todos os dados da tabela;</li>
  <li> Exemplo:
        DELETE FROM tabela
        WHERE condicao;</li>
</ul>

