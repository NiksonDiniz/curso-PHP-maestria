<div class="titulo">Criando banco de dados</div>

<ul>
  <li>Podemos criar bancos de dados manualmente em softwares como o phpMyAdmin ou por SQL;</li>
  <li>Neste curso, com o intuito de aprender SQL, vamos utilizar os comandos de query que vão servir ao longo da sua carreira de programação;</li>
  <li>Para criar bancos vamos utilizar o: <br>
      CREATE DATABASE nomedobanco;</li>
</ul>

<div class="titulo">Removendo bancos</div>

<ul>
  <li>Podemos também remover os bancos, ou seja deletá-los do sistema;</li>
  <li>Isso fará com que todos os dados e tabelas sejam perdidos, então tome cuidado;</li>
  <li>O comando para deletar bancos é: <br>
      DROP DATABASE nomedobanco;</li>
</ul>
