<div class="titulo">Principais tipos de dados</div>

<ul>
  <li>Os tipos de dados do banco funcionam como os tipos de dados de variáveis, porém em vários ‘níveis’ para a melhor performance;</li>
  <li>VARCHAR: texto de 0 a 65535 caracteres;</li>
  <li>TEXT: texto com no máximo 65535 bytes;</li>
  <li>INT: números inteiros;</li>
  <li>BIGINT: números inteiros com maior proporção que o INT;</li>
  <li>DATE: data no formato YYYY-MM-DD</li>
</ul>