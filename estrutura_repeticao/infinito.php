<div class="titulo">Loop infinito</div>

<ul>
  <li>O loop infinito é um erro que pode ser ocasionado quando uma estrutura de repetição não tem uma condição de término que seja possível;</li>
  <li>Por exemplo: x > 10 e a variável de referência tem um decremento, não um incremento;</li>
  <li>Isso vai fazer o software travar, e pode ser um grande problema caso usuários estejam acessando o mesmo;</li>
</ul>