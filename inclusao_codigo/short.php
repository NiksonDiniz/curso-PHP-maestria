<div class="titulo">Short tags</div>

<ul>
  <li>A short tag é uma funcionalidade para adicionar código PHP em uma página;</li>
  <li>Este recurso depende de uma configuração do servidor para funcionar;</li>
  <li>Por isso é desencorajado seu uso, pode ser que o código não funcione;</li>
  <li>Ex: <'? echo "teste"; ?></li>
</ul>

